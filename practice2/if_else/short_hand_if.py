# 1. Single line if
a = 10
if a > 5: print("a is greater than 5")

# 2. Single line if-else
b = 3
print("b is even") if b % 2 == 0 else print("b is odd")

# 3. Multiple condition shorthand
x = 8
print("Positive") if x > 0 else print("Zero or Negative")

# 4. Inline comparison
num = 12
print("Large") if num > 10 else print("Small")

# 5. Check even/odd in one line
n = 7
print("Even") if n % 2 == 0 else print("Odd")
