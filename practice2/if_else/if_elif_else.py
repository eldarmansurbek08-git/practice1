# 1. Grading system
score = 85
if score >= 90:
    print("Excellent")
elif score >= 70:
    print("Good")
else:
    print("Needs improvement")

# 2. Age category
age = 25
if age < 13:
    print("Child")
elif age < 20:
    print("Teen")
else:
    print("Adult")

# 3. Temperature status
temp = 0
if temp > 30:
    print("Hot")
elif temp > 10:
    print("Warm")
else:
    print("Cold")

# 4. Traffic light
light = "yellow"
if light == "red":
    print("Stop")
elif light == "yellow":
    print("Get ready")
else:
    print("Go")

# 5. Number sign
number = -10
if number > 0:
    print("Positive")
elif number < 0:
    print("Negative")
else:
    print("Zero")
